RCON = (0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36)


def _xtime(b: int) -> int:
    return ((b << 1) ^ 0x1b) & 0xff if b & 0x80 else (b << 1) & 0xff


def _gmul(a: int, b: int) -> int:
    r = 0
    while b:
        if b & 1:
            r ^= a
        a = _xtime(a)
        b >>= 1
    return r & 0xff


def key_expansion_256(key: bytes) -> list[bytes]:
    """Expand 32-byte key into 15 round keys (16 bytes each) per FIPS 197."""
    if len(key) != 32:
        raise ValueError(f"AES-256 requires a 32-byte key, got {len(key)}")
    Nk, Nr, Nb = 8, 14, 4
    w = [list(key[4 * i: 4 * i + 4]) for i in range(Nk)]
    for i in range(Nk, Nb * (Nr + 1)):
        t = list(w[i - 1])
        if i % Nk == 0:
            t = [SBOX[t[1]], SBOX[t[2]], SBOX[t[3]], SBOX[t[0]]]
            t[0] ^= RCON[i // Nk]
        elif i % Nk == 4:
            t = [SBOX[b] for b in t]
        w.append([w[i - Nk][j] ^ t[j] for j in range(4)])
    rks = []
    for r in range(Nr + 1):
        rk = []
        for c in range(4):
            rk.extend(w[r * 4 + c])
        rks.append(bytes(rk))
    return rks


def _sub_bytes(s: bytearray) -> None:
    for i in range(16):
        s[i] = SBOX[s[i]]


def _shift_rows(s: bytearray) -> None:
    x = bytes(s)
    s[0], s[4], s[8], s[12]  = x[0],  x[4],  x[8],  x[12]
    s[1], s[5], s[9], s[13]  = x[5],  x[9],  x[13], x[1]
    s[2], s[6], s[10], s[14] = x[10], x[14], x[2],  x[6]
    s[3], s[7], s[11], s[15] = x[15], x[3],  x[7],  x[11]


def _mix_columns(s: bytearray) -> None:
    for c in range(4):
        o = c * 4
        a, b, cc, d = s[o], s[o + 1], s[o + 2], s[o + 3]
        s[o]     = _gmul(2, a) ^ _gmul(3, b) ^ cc ^ d
        s[o + 1] = a ^ _gmul(2, b) ^ _gmul(3, cc) ^ d
        s[o + 2] = a ^ b ^ _gmul(2, cc) ^ _gmul(3, d)
        s[o + 3] = _gmul(3, a) ^ b ^ cc ^ _gmul(2, d)


def _add_round_key(s: bytearray, rk: bytes) -> None:
    for i in range(16):
        s[i] ^= rk[i]


def encrypt_block(plaintext: bytes, round_keys: list[bytes]) -> bytes:
    if len(plaintext) != 16:
        raise ValueError("AES block must be 16 bytes")
    s = bytearray(plaintext)
    _add_round_key(s, round_keys[0])
    for r in range(1, 14):
        _sub_bytes(s)
        _shift_rows(s)
        _mix_columns(s)
        _add_round_key(s, round_keys[r])
    _sub_bytes(s)
    _shift_rows(s)
    _add_round_key(s, round_keys[14])
    return bytes(s)


def aes_256_ctr(data: bytes, key: bytes, nonce: bytes) -> bytes:
    """AES-256-CTR. Nonce is the initial 128-bit counter (big-endian).

    Encryption and decryption are identical (XOR with keystream).
    """
    if len(key) != 32:
        raise ValueError("AES-256 key must be 32 bytes")
    if len(nonce) != 16:
        raise ValueError("CTR nonce must be 16 bytes")
    rks = key_expansion_256(key)
    counter = int.from_bytes(nonce, "big")
    out = bytearray(len(data))
    for off in range(0, len(data), 16):
        ks = encrypt_block(counter.to_bytes(16, "big"), rks)
        chunk = data[off: off + 16]
        for i, b in enumerate(chunk):
            out[off + i] = b ^ ks[i]
        counter = (counter + 1) & ((1 << 128) - 1)
    return bytes(out)
